# Inteiro
numero_inteiro = 3
print("Inteiro =", numero_inteiro)

# Real com ponto flutuante
numero_real = 3.14
print("Numero real =", numero_real)

# type()
print("Tipo da variavel do inteiro:", type(numero_inteiro))
print("Tipo da variavel do real:", type(numero_real))

# soma +
soma = 1 + 1
print("Soma de 1 + 1:", soma)

# subtração -
soma = 1 - 1
print("Subtracao de 1 - 1:", soma)

# multiplicação *
soma = 2 * 10
print("Multiplicacao de 2 x 10:", soma)

# divisão /
soma = 9 / 2
print("Divisao de 9 / 2:", soma)
print(type(soma))
soma2 = 10 / 2
print("Divisao de 10 / 2:", soma2)
print(type(soma2))

# int()
print("Valor em int:", int(soma2))

# float()
print("Valor em float:", float(soma2))

# Modulo % (resto da divisão)
modulo = 5 % 2
print(modulo)
modulo = 4 % 2
print(modulo)
